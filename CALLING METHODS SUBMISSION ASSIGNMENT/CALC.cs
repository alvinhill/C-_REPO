using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CALLING_METHODS_SUBMISSION_ASSIGNMENT
{
    class CALC
    {

        // DEFINE VARIABLES
        public int numU;
        public int numMU = 10;
        public int numC = 100;
        public int numSC;
        public int numMC;
        public int numSUS = 10;

        // ADDITION SECTION
        public int Addnum()
        {
            return numU + numC;
        }

        // SUBTRACTION SECTION
        public int Subnum()
        {
            return numSC - numSUS;
        }

        // MULTIPLICATION SECTION
        public int Multnum()
        {
            return numMU * numMC;
        }






















    }
}
